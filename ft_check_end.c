#include "libft.h"

size_t	ft_check_end(char const *c)
{
	size_t i;

	i = ft_strlen(c) - 1;
	while (i > 0)
	{
		if (c[i] != ' ' && c[i] != '\n' && c[i] != '\t')
			return (i + 1);
		i--;
	}
	return (ft_strlen(c));
}
