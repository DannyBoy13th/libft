#include "libft.h"

void	ft_putnbr(int n)
{
	long long j;

	j = n;
	if (j < 0)
	{
		j = j * -1;
		ft_putchar(45);
	}
	if (j >= 10)
	{
		ft_putnbr(j / 10);
		ft_putnbr(j % 10);
	}
	else
		ft_putchar(j + '0');
}
