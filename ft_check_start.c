#include "libft.h"

size_t	ft_check_start(char const *c)
{
	size_t i;

	i = 0;
	while (c[i] != '\0')
	{
		if (c[i] != ' ' && c[i] != '\n' && c[i] != '\t')
			return (i);
		i++;
	}
	return (i);
}
